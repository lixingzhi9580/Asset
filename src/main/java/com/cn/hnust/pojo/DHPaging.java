package com.cn.hnust.pojo;

public class DHPaging {
	private int num_end;
	private int next_page;
	private int total_num;
	private int total_pages;
	private int pre_page;
	private int number_per_page;
	private int pager_num_amount;
	private int cur_page;
	private int num_begin;
	public int getNum_end() {
		return num_end;
	}
	public void setNum_end(int num_end) {
		this.num_end = num_end;
	}
	public int getNext_page() {
		return next_page;
	}
	public void setNext_page(int next_page) {
		this.next_page = next_page;
	}
	public int getTotal_num() {
		return total_num;
	}
	public void setTotal_num(int total_num) {
		this.total_num = total_num;
	}
	public int getTotal_pages() {
		return total_pages;
	}
	public void setTotal_pages(int total_pages) {
		this.total_pages = total_pages;
	}
	public int getPre_page() {
		return pre_page;
	}
	public void setPre_page(int pre_page) {
		this.pre_page = pre_page;
	}
	public int getNumber_per_page() {
		return number_per_page;
	}
	public void setNumber_per_page(int number_per_page) {
		this.number_per_page = number_per_page;
	}
	public int getPager_num_amount() {
		return pager_num_amount;
	}
	public void setPager_num_amount(int pager_num_amount) {
		this.pager_num_amount = pager_num_amount;
	}
	public int getCur_page() {
		return cur_page;
	}
	public void setCur_page(int cur_page) {
		this.cur_page = cur_page;
	}
	public int getNum_begin() {
		return num_begin;
	}
	public void setNum_begin(int num_begin) {
		this.num_begin = num_begin;
	}

}
