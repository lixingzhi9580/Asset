package com.cn.hnust.pojo;

public class KangXing {
	private double hl;
	private double fy;
	private double hs;
	private double yw;
	public double getHl() {
		return hl;
	}
	public void setHl(double hl) {
		this.hl = hl;
	}
	public double getFy() {
		return fy;
	}
	public void setFy(double fy) {
		this.fy = fy;
	}
	public double getHs() {
		return hs;
	}
	public void setHs(double hs) {
		this.hs = hs;
	}
	public double getYw() {
		return yw;
	}
	public void setYw(double yw) {
		this.yw = yw;
	}
}
